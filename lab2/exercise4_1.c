#include <stdio.h>

int main()
{
    for (int i=1;i<=10;i++){
        printf("%d\n",i);
    }
    return 0;
}

//for loop syntax: for(initialization;condition;increment)
//initialization: runs once at the start (i=1)
//condition: checked before each iteration (i<=10)
//increment: runs after each iteration (i++) - i++ == i=i+1